package org.brekka.stillingar.api.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Can be applied to a parameter of a {@link ConfigurationListener} method to indicate that the parameter should be
 * called with the previous value in the event of an update, or null if this is the first invocation.
 *
 * @author Andrew Taylor
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ ElementType.PARAMETER })
public @interface Previous {

}
